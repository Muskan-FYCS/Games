    <section id="plank-walk-details">
         <h3>How It Works:</h3>
        <h4>Team Formation:</h4>
        <p>Divide participants into four groups, each consisting of five members. Assign a start point and a finish line for the Plank Walk Challenge.</p>
        
        <h4>Plank and Rope Setup:</h4>
        <p>Each group is provided with a plank, and a rope is securely attached to the plank. The length of the rope allows each team member to comfortably hold on to it while walking.</p>
        
        <h4>Starting Line:</h4>
        <p>All groups assemble at the starting line with their planks and ropes. The objective is to reach the finish line while keeping the plank level and parallel to the ground.</p>
        
        <h4>Walking Technique:</h4>
        <p>The challenge begins with the first team member holding the rope and walking forward, guiding the plank along with them. Subsequent team members follow suit, holding onto the rope and maintaining coordination to keep the plank level.</p>
        
        <h4>Team Coordination:</h4>
        <p>Effective communication and synchronization are key as team members adjust their pace, ensuring the plank stays parallel to the ground. Teams must navigate any turns or obstacles in the path without letting the plank touch the ground.</p>
        
        <h4>Finish Line:</h4>
        <p>The race concludes when all team members successfully reach the finish line with the plank, keeping it off the ground. A referee or facilitator monitors each group's performance.</p>
        
        <h4>Winning Team:</h4>
        <p>The team that completes the Plank Walk Challenge in the shortest time with the plank level and off the ground is declared the winner.</p>
        </section>
        
        <section id="plank-walk-details">
        <h3>Key Features:</h3>
        <ul>
            <li><strong>Teamwork:</strong> The challenge promotes teamwork as members collaborate to keep the plank level and reach the finish line.</li>
            <li><strong>Communication Skills:</strong> Clear and effective communication is crucial for coordinating movements and maintaining balance.</li>
            <li><strong>Precision and Coordination:</strong> Participants must focus on precision and coordination to prevent the plank from touching the ground.</li>
            <li><strong>Strategy:</strong> Teams can strategize on the best way to distribute roles and navigate the course efficiently.</li>
        </ul>
        </section>
        
        
        <section id="plank-walk-details">
