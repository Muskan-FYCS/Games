        <h2>About the Plank Walk Challenge</h2>
        <p>
            Welcome to the Plank Walk Challenge, an exhilarating team-building activity that combines coordination, communication, and precision. In this challenge, groups of five people will embark on a race, each holding a rope attached to a plank, navigating their way from the start point to the finish line. Let the plank walking begin!
        </p>
