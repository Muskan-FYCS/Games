        <p>
           Welcome to the Tic Tac Balloon Relay, a lively and entertaining variation of the
classic X & O game. In this exhilarating team-building activity, groups of 10 members
each will bring the timeless game to life, with a balloon-filled relay that adds laughter,
strategy, and movement to the mix.</p>
    </section>
    <section id="Tic Tac Balloon Relay-details">
        <h3>How It Works:</h3>
        <h4>Team Formation:</h4>
        <p>• Divide participants into two teams, each consisting of 10 members.
• Assign roles within the teams: five members will represent "X," and five
members will represent "O."</p>
        
        <h4>Balloon Relay Setup:</h4>
        <p>• Place a large Tic Tac Toe grid on the ground, with each square marked
clearly.
• Behind each team, set up a starting line and a designated distance that each
team must travel to complete the relay.</p>
        
        <h4>Starting the Relay:</h4>
        <p>ting the Relay:
• Each team selects two members at a time to participate in the relay.
• One member holds the "X" balloon, and the other holds the "O" balloon.</p>
        
        <h4>Game Strategy:</h4>
         <p>• The relay begins with the first pair of team members making their way to the
Tic Tac Toe grid.
• The member holding the "X" balloon places it in an empty square on the grid.
• After placing the balloon, the team members return to their teammates.</p>
        
        <h4>Balloon Handover:</h4>
        <p>The next pair of team members takes their turn, with one holding the "X"
balloon and the other holding the "O" balloon.
• They strategically place their balloons in adjacent squares, aiming to complete
a row horizontally, vertically, or diagonally.
</p>
        
        <h4>Continuing the Relay:
</h4>
        <p>The relay continues until one team successfully completes a row of "X" or "O."
• The team that achieves this goal must complete an additional challenge, such
as popping a celebratory balloon, to declare victory.</p>
        
        <h4>Winning Team:</h4>
        <p>The team that completes a row and successfully finishes the relay is declared
the winner of the Tic Tac Balloon Relay</p>
        </section>
        
        <section id="Tic Tac Balloon Relay-details">
