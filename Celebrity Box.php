        <h2>About the Celebrity Box Puzzle Challenge</h2>
        <p>
            Welcome to the Celebrity Box Puzzle Challenge, a delightful and creative teambuilding activity that combines the love of celebrities with the thrill of puzzle-solving.
In this challenge, teams of 10 members each will work together to assemble a
celebrity face using a set of 16 cube pieces. Each side of the cube features a photo
piece of a celebrity, and the team that successfully completes the face first will be
crowned the Celebrity Box Puzzle Champions!

        </p>
    </section>

   

    <section id="Celebrity Box Puzzle">
         <h3>How It Works:</h3>
        <h4>Team Formation:</h4>
        <p>
• Divide participants into two teams, each comprising 10 members.
• Prepare a set of 16 cube pieces with celebrity photos on each side.</p>
        
        <h4>Puzzle Setup:</h4>
        <p>•Assemble the cube pieces in a way that, when stacked, they create a full-face
image of a celebrity on one side.
• Each team receives an identical set of cube pieces with the same celebrity
face to complete</p>
        
        <h4>Puzzle Assembly:
</h4>
        <p>On a signal, teams begin assembling the cube pieces one by one to recreate
the celebrity face.</p>
        
        <h4>Communication and Coordination:</h4>
        <p>Effective communication is key as team members discuss and strategize the
placement of each cube piece.
• Coordination is crucial to ensure that the pieces are stacked in the correct
order to form the complete celebrity face.</p>
        
        <h4>Obstacles and Challenges:
</h4>
        <p>• Introduce additional challenges or obstacles to add excitement and
complexity.
• For example, restrict verbal communication for a certain duration or blindfold
some team members to make the task more challenging.
</p>
        
        <h4>Finish Line:</h4>
        <p>The challenge concludes when one team successfully completes the celebrity
face by stacking all 16 cube pieces in the correct order</p>
        
        <h4>Winning Team:</h4>
        <p>The team that finishes assembling the celebrity face first is declared the
winner of the Celebrity Box Puzzle Challenge.
</p>
        </section>
        
