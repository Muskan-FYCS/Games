    <section class="how-it-works">
        <h2>How It Works</h2>
        <h3>Team Formation:</h3>
          <p>Divide participants into two teams, each comprising 10 members. Designate a starting point and a target area for each team.</p>
        
        <h3>Tied Hands Setup:</h3>
        <p>Tie the hands of each team member at the back, creating an equal playing field where everyone faces the challenge with hands restrained.</p>
        
        <h3>Ball Placement:</h3>
        <p>Place a ball at the starting point equidistant from both teams. Designate target areas or goals on opposite sides where each team aims to push the ball.</p>
        
        <h3>Starting Line:</h3>
        <p>All teams assemble at the starting line, ready for the challenge with their hands tied behind their backs. The objective is to push the ball from the starting point to the target area.</p>
        
        <h3>Pushing Relay:</h3>
        <p>On a signal, both teams start pushing the ball using any part of their bodies except their hands. Teams must work together to strategize the best way to move the ball forward.</p>
        
        <h3>Obstacles and Challenges:</h3>
        <p>Introduce obstacles or challenges along the way to add excitement and strategy. Teams must navigate turns, avoid obstacles, and potentially tackle opponents to hinder their progress.</p>
        <h3>Target Area:</h3>
        <p>The challenge concludes when the team successfully pushes the ball into the designated target area on the opposite side. A referee or facilitator oversees the challenge, ensuring fair play.</p>
        
        <h3>Winning Team:</h3>
        <p>The team that successfully pushes the ball into the target area first is declared the winner of the Tied Hands Ball Push Challenge.</p>
    </section>

    <section class="key-features">
