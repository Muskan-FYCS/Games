    <section class="challenge-details">
        <h2>About the Lava Walk Challenge</h2>
        <p>
            Welcome to the Lava Walk Challenge, a thrilling and strategic team-building activity that will test your group's coordination, communication, and problem-solving skills. In this challenge, groups of 10 people will navigate an imaginary "lava" terrain, using only pieces of paper to avoid touching the ground. Let the Lava Walk begin!
        </p>
    </section>

    <section id="lava-walk-details">
        <h3>How It Works:</h3>
        <h4>Team Formation:</h4>
        <p>Divide participants into four groups, each consisting of 10 members. Designate a start point and an end point for the Lava Walk Challenge.</p>
        
        <h4>Lava Terrain Setup:</h4>
        <p>Imagine the ground as "hot lava" that participants must avoid touching. Provide each team with 10 pieces of paper, which act as stepping stones.</p>
        
        <h4>Starting Line:</h4>
        <p>All groups assemble at the starting line with their pieces of paper. The challenge is to strategically place and move across the "lava" using only the paper.</p>
        
        <h4>Paper Placement:</h4>
        <p>The first participant places a piece of paper on the ground and steps onto it. Subsequent team members follow suit, placing and stepping on pieces of paper, creating a pathway across the "lava."</p>
        
        <h4>Team Coordination:</h4>
        <p>Effective communication and collaboration are crucial for the team to coordinate their movements and ensure everyone has a stable path.</p>
        
        <h4>Challenges and Obstacles:</h4>
        <p>Introduce obstacles or challenges along the way that require additional strategy and problem-solving skills. For example, narrow passages, limited pieces of paper, or areas where team members must collaborate closely.</p>
        
        <h4>Finish Line:</h4>
         <p>The challenge concludes when all team members successfully reach the finish line using only the pieces of paper and without touching the "lava."</p>
        
        <h4>Winning Team:</h4>
        <p>The team that completes the Lava Walk Challenge in the shortest time with all members reaching the finish line is declared the winner.</p>
        </section>
        
        <section id="lava-walk-details">
        <h3>Key Features:</h3>
        <ul>
            <li><strong>Teamwork:</strong> The challenge emphasizes teamwork as participants collaborate to create a pathway and safely navigate the "lava."</li>
            <li><strong>Communication Skills:</strong> Clear and effective communication is vital for coordinating movements and overcoming obstacles.</li>
            <li><strong>Problem-Solving:</strong> Participants must strategize to overcome challenges and efficiently use the limited pieces of paper.</li>
            <li><strong>Adaptability:</strong> The challenge encourages adaptability as teams adjust their approach based on the evolving terrain.</li>
        </ul>
        </section>
        
        <section id="lava-walk-details">
        <h3>Conclusion:</h3>
        <p>The Lava Walk Challenge is not just a test of physical coordination; it's a dynamic and engaging activity that pushes teams to think strategically and work together to conquer the "lava." So, gather your teams, grab those pieces of paper, and step into the realm of imagination for a thrilling journey! May the most strategic team emergevictorious and conquer the lava!</p>
