     <section id="pen-ball-details">
        <h3>How It Works:</h3>
        <h4>Team Formation:</h4>
        <p>Form four teams, each comprising 6 members. Designate a distinct start point and end point for the Pen and Ball Team Challenge.</p>
        
        <h4>Pen and Ball Setup:</h4>
        <p>Each team member holds a pen in each hand, creating a "gripper" effect. A ball is strategically placed between the pens, ensuring it is secure but also challenging to balance.</p>
        
        <h4>Starting Line:</h4>
        <p>All teams gather at the starting line, ready to embark on the challenge with their Pen and Ball setups. The objective is to navigate from the start point to the end point as a cohesive team, ensuring the ball remains in place.</p>
        
        <h4>Team Coordination:</h4>
        <p>On a signal, all team members start walking in unison, holding the Pen and Ball structure. Coordination is crucial as the team moves together to prevent the ball from dropping.</p>
        
        <h4>Unity in Motion:</h4>
        <p>Teams must maintain unity and harmony in their movements, adapting to turns, obstacles, and challenges along the course. Communication is key to ensure everyone is on the same page.</p>
        
        <h4>Handover and Strategy:</h4>
        <p>After covering a segment of the course, the team may choose to strategically hand over the Pen and Ball structure to different members. Teams can strategize and plantheir approach based on the dynamics of their group.</p>
        
        <h4>Finish Line:</h4>
        <p>The challenge concludes when the entire team successfully reaches the finish line without the ball dropping. Teams must be cautious and deliberate in their movements to achieve success.</p>
        
        <h4>Winning Team:</h4>
        <p>The team that completes the Pen and Ball Team Challenge in the shortest time and with the ball intact is declared the winner.</p>
