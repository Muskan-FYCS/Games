<section class="challenge-details">
        <h2>About the Challenge</h2>
        <p>
            Welcome to the timeless and energetic game of Red Light, Green Light – a classic that never fails to bring joy, laughter, and a hint of suspense. Whether you're organizing a children's party, a team-building event, or just want to relive the nostalgia of your childhood, this game is a perfect choice for all ages.
            
        </p>
        <p> <strong>Objective:</strong> The goal of Red Light, Green Light is simple – players aim to reach a designated finish line by responding to commands indicating when to move and when to stop. Sounds easy, right? Well, it's the twist in the rules that adds an exciting element of strategy and anticipation.</p> 
        </section> 
        <section Class="red-light-green-light">
         <h3>How to Play:</h3> 
         <h4>Setting Up:</h4> 
         <p>Designate a clear playing area with a starting line and a finish line. Choose one player to be the "Traffic Light" or "Caller."</p> 
         <h4>Positions:</h4>
          <p>All players stand behind the starting line, waiting for the Traffic Light's commands.</p> 
          <h4>Commands:</h4> 
          <ul> 
          <li>
          <strong>Green Light:</strong> Players move forward as fast as they can towards the finish line.</li> <li><strong>Red Light:</strong> The Traffic Light calls out "Red Light!" and turns around to face the other way. At this command, players must freeze and stop moving immediately.</li> <li><strong>Yellow Light (Optional):</strong> To add an extra layer of excitement, you can introduce a Yellow Light where players move in slow motion.</li> </ul> <h4>Catching Players:</h4> <p>If the Traffic Light catches a player still moving after calling "Red Light," that player is typically sent back to the starting line.</p> 
          <h4>Winning:</h4> 
          <p>The first player to reach the finish line becomes the next Traffic Light for the next round.</p> 
          </Section>
          
          <section class="red-light-green-light">
            <h3>Key Skills Developed:</h3>
           <ul> 
           <li><strong>Listening Skills:</strong> Players must listen attentively for the commands to know when to move and when to stop.</li>
            <li><strong>Coordination and Reaction Time:</strong> Reacting quickly to the changing commands requires good coordination and rapid decision-making.</li>
             <li><strong>Strategy:</strong> Choosing when to take big steps or move cautiously is part of the strategy to avoid getting caught.</li>
             <li><strong>Sportsmanship:</strong> Celebrating others' successes and handling setbacks with a positive attitude are important aspects of the game.</li> 
             </ul>
             </section>
    
    <section class="red-light-green-light">
    <h3>Variations:</h3>
           <ul> 
           <li><strong>Giant Red Light, Green Light:</strong> Play the game on a larger scale, suitable for outdoor events or larger spaces.</li>
            <li><strong>Themed Red Light, Green Light: </strong> Customize the commands and rules to fit a specific theme, making the game even more entertaining.</li> 
             </ul>
    </section>
    
