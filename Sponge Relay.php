        <h2>About the Sponge Relay Challenge</h2>
        <p>
            Welcome to the Sponge Relay Challenge, a refreshing and engaging team activity that combines coordination, speed, and a splash of fun. In this challenge, teams of 10 members each will embark on a journey from one point to another, passing a sponge filled with water to fill a bottle on the opposite side. The team that fills the bottle the most in the quickest time will be crowned the victorious Sponge Soakers!
        </p>
    </section>
     <section id="sponge-relay-details">
        <h3>How It Works:</h3>
        <h4>Team Formation:</h4>
        <p>Divide participants into four teams, each comprising 10 members. Designate a clear start point and an end point for the Sponge Relay Challenge.</p>
        
        <h4>Materials Setup:</h4>
        <p>Provide each team with a sponge and a water-filled bucket at the start point. Place empty bottles at the end point where the teams need to fill them.</p>
        
        <h4>Starting Line:</h4>
        <p>All teams gather at the starting line with their sponges and water-filled buckets. The objective is to transfer water using the sponge to fill the bottle at the end point.</p>
        
        <h4>Relay Race:</h4>
        <p>On a signal, the first team member soaks the sponge in the water bucket and starts walking towards the end point. The team member then squeezes the water from the sponge into the bottle, making sure to fill it as much as possible.</p>
        
        <h4>Passing the Sponge:</h4>
        <p>After the first team member fills the bottle, they pass the wet sponge to the next team member. The relay continues in this manner, with each team member taking turns soaking the sponge, walking to the bottle, and filling it.</p>
        
        <h4>Strategy and Speed:</h4>
        <p>Teams must strategize on the best way to transport the water, considering factors like speed, precision, and teamwork. The speed of the relay is crucial, as teams aim to fill the bottle as much as possible within a set time.</p>
        
        <h4>Finish Line:</h4>
        <p>The challenge concludes when all team members have completed the relay, and the bottles are filled to the brim. The referee or facilitator measures and records the water level in each team's bottle.</p>
        
        <h4>Winning Team:</h4>
        <p>The team that fills their bottle the most within the specified time is declared the winner of the Sponge Relay Challenge.</p>
        </section>
        
        <section id="sponge-relay-details">
        <h3>Key Features:</h3>
        <ul>
            <li><strong>Team Coordination:</strong> The challenge emphasizes teamwork as participants collaborate to transport and fill the bottle with water.</li>
            <li><strong>Communication:</strong> Clear communication is vital for the smooth transfer of the wet sponge and efficient filling of the bottle.</li>
            <li><strong>Speed and Precision:</strong> Teams need a balance of speed and precision to maximize the amount of water transferred within the time limit.</li>
            <li><strong>Strategic Thinking:</strong> Teams can strategize on the most effective way to pass the sponge and fill the bottle, considering the unique dynamics of their group.</li>
        </ul>
